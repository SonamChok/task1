class dog:
    def _init_(self,name,colour,age):
        self.name=name
        self.colour=colour
        self.age=age
dawa=dog("dawa","red",3)
nima=dog ("nima", "white",2)
blacky=dog ("blacky", "black", 1)

print(dawa.name, dawa.colour,dawa.age)
print(nima.name,nima.colour,nima.age)
print(blacky.name,blacky.colour,blacky.age)